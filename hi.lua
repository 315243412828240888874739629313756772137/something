    part.CFrame = CFrame.new(targetPosition)
end

local function processGemQueue()
    while true do
        if #gemQueue > 0 then
            local closestGem = gemQueue[1]
            for _, gem in ipairs(gemQueue) do
                if (HumanoidRootPart.Position - gem.Position).Magnitude < (HumanoidRootPart.Position - closestGem.Position).Magnitude then
                    closestGem = gem
                end
            end

            teleportToPosition(HumanoidRootPart, closestGem.Position)

            table.remove(gemQueue, table.find(gemQueue, closestGem))

            wait(0.1)
        else
            wait(0.1)
        end
    end
end

