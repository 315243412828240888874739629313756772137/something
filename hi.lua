local gemQueue = {}

local function addGemToQueue(gem)
    if gem and gem.Parent then
        if gem:IsA("BasePart") and gem.Position and gem.Size.Magnitude > 0 then
            if not table.find(gemQueue, gem) then
                table.insert(gemQueue, gem)
            end
        end
    end
end

local function monitor()
    workspace.DescendantAdded:Connect(function(obj)
        if obj:IsA("BasePart") and obj.Name:lower():find("gem") then
            addGemToQueue(obj)
        end
    end)
end

local function tweenToPosition(part, targetPosition, duration)
    local tweenInfo = TweenInfo.new(duration, Enum.EasingStyle.Linear, Enum.EasingDirection.InOut)
    local goal = {CFrame = CFrame.new(targetPosition)}
    local tween = TweenService:Create(part, tweenInfo, goal)
    tween:Play()
    tween.Completed:Wait()
end

local function calculateDuration(startPosition, endPosition)
    local distance = (startPosition - endPosition).Magnitude
    local baseSpeed = 200
    return distance / baseSpeed
end

local function sortGemsByDistance()
    table.sort(gemQueue, function(a, b)
        local distanceA = (HumanoidRootPart.Position - a.Position).Magnitude
        local distanceB = (HumanoidRootPart.Position - b.Position).Magnitude
        return distanceA < distanceB
    end)
end

local function process()
    while true do
        if #gemQueue > 0 then
            sortGemsByDistance()
            
            local gem = table.remove(gemQueue, 1)
            if gem and gem.Parent and gem:IsA("BasePart") and gem.Size.Magnitude > 0 then
                local duration = calculateDuration(HumanoidRootPart.Position, gem.Position)
                tweenToPosition(HumanoidRootPart, gem.Position, duration)
            end
        else
            wait(0.1)
        end
    end
end

local function initialize()
    for _, obj in pairs(workspace:GetDescendants()) do
        if obj:IsA("BasePart") and obj.Name:lower():find("gem") then
            addGemToQueue(obj)
        end
    end

    monitor()
    process()
end

initialize()

	end    
